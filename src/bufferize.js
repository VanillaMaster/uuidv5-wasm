/**
 * @param { Uint32Array } arr 
 * @returns { Uint8Array }
 */
export function bufferize(arr) {
    const [i0 , i1, i2, i3] = arr;
    return Uint8Array.of(
        i0 >> 0x18 & 0xFF,
        i0 >> 0x10 & 0xFF,
        i0 >> 0x08 & 0xFF,
        i0 >> 0x00 & 0xFF,
        i1 >> 0x18 & 0xFF,
        i1 >> 0x10 & 0xFF,
        i1 >> 0x08 & 0x0F | 0x50,
        i1 >> 0x00 & 0xFF,
        i2 >> 0x18 & 0x3F | 0x80,
        i2 >> 0x10 & 0xFF,
        i2 >> 0x08 & 0xFF,
        i2 >> 0x00 & 0xFF,
        i3 >> 0x18 & 0xFF,
        i3 >> 0x10 & 0xFF,
        i3 >> 0x08 & 0xFF,
        i3 >> 0x00 & 0xFF
    );
}